def encode(plaintext, key)
  cipher = key.chars.uniq + (('a'...'z').to_a - key.chars)
  ciphertext_chars = plaintext.chars.map do |char|
    # the second part of this equation is returning a 'nil' at some point - try printing the cipher to see why that might be
    (65 + cipher.find_index(char)).chr
  end
  ciphertext_chars.join
end

def decode(ciphertext, key)
  cipher = key.chars.uniq + (('a'...'z').to_a - key.chars)
  plaintext_chars = ciphertext.chars.map do |char|
    # the decode is no longer returning an error - but is returning the wrong letters. try printing the results of .ord in this method to the results of .chr in the first method - can you see a pattern in the way they are mismatched?
    cipher[65 - char.ord]
  end
  plaintext_chars.join
end

